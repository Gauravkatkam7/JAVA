package loops;

public class forloop

//print no from 1to10
//start no=1
// end no=10
//increment by 1 no

{
public static void main(String[] args) {
	
	for(int i=1; i<=10; i++)
	{
		System.out.println(i);
	}
	
	
	//increment by 2 no
	
		for(int i=1; i<=10; i=i+2)
		{
			System.out.println(i);
		}
		
}
}
