package controlstatements;

public class switch7 {
public static void main(String[] args) {
	
	switch(5)
	{
	case 1:System.out.println("cd");
	break;
	
	case 2:System.out.println("cw");
	break;
	
	case 3: System.out.println("bi");
	break;
	
	case 4: System.out.println("sa");
	break;
	
	default: System.out.println("invalid input");
	}
}
}
