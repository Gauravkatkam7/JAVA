package controlstatements;

public class switch9 {
public static void main(String[] args) {
	
	switch(5)
	{
	case 1: System.out.println("computer");
	break;
	
	case 2: System.out.println("cpu");
	break;
	
	case 3: System.out.println("keyboard");
	break;
	
	case 4: System.out.println("screen");
	break;
	
	default: System.out.println("not available");
	}
}
}
