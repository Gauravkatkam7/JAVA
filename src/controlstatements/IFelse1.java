package controlstatements;

public class IFelse1 {
	public static void main(String[] args) {
		
		int marks=50;
		
	

	if(marks>=35)                   //if (condition) -->true
		{
		System.out.println("pass");
		
	}
	
	else 
	{
		System.out.println("fail"); //if (condition) -->false
	}
	
}
}