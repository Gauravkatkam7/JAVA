package assignments;

public class swapingnum1 {

	public static void main(String[] args) {
		
		int first=10;
		int second=20;
		int samp;
		
		System.out.println("before swap :");
		System.out.println(first);
		System.out.println(second);
		
	    samp=first;//10
		first=second;//20
		second=samp;//10
		
		System.out.println("after swap:");
		System.out.println(first);
		System.out.println(second);
		
	}
}
